friends = 5

# Arithmetic operators are used to perform mathematical operations on variables and values. Here are some common arithmetic operators in Python:

#friends = friends + 2
#print(friends) - output: 7

# The addition operator (+) returns the sum of the left and right operands.

friends += 2
print(friends) # output: 7

# The subtraction operator (-) returns the difference of the left and right operands.

friends -= 3
print(friends) # output: 4

# The multiplication operator (*) returns the product of the left and right operands.

friends *= 2 
print(friends) # output: 8

# The division operator (/) returns the quotient of the division of the left operand by the right operand.

friends /= 4
print(friends) # output: 2.0

# The exponentiation operator (**) raises the left operand to the power of the right operand.

friends **= 3
print(friends) # output: 125

# The modulus operator (%) returns the remainder of the division of the left operand by the right operand.

remainder = friends % 2
print(remainder) # output: 1
