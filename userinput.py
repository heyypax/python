# Input() function is used to take input from the user. It always returns a string, so if you want to take a number as input, you need to convert it to the appropriate type (int or float).

name = input("What is your name? ")
age = int(input("What is your age? "))
age = age + 1
print(f"Hello {name}!")
print("HAPPY BIRTHDAYY!")
print(f"You are {age} years old now.")


